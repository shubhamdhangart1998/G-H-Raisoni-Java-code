package Logic;

public class EvenOddChecker {
	 public static void main(String[] args) {
	        int number = 15;

	        if (number % 2 == 0) {
	            System.out.println(number + " is an even number.");
	        } else {
	            System.out.println(number + " is an odd number.");
	        }
	    }
}
